public class ProfileActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        setContentView(R.layout.profile_screen);

        Profile user = (Profile) getIntent().getSerializableExtra("User"); // user from MainActivity

        // Makes autogenerated profile picture
        Bitmap pfpBitmap = Image.generateProfilePicture(user.getName(), 200, Color.parseColor("#3351A6"), Color.WHITE);
        // Grabs URI of saved profile picture image
        Uri savedUri = ProfilePicture.saveProfilePicture(this, pfpBitmap, user.getName());

        ImageView profile_picture = findViewById(R.id.profile_picture );
        // Sets the profile picture as the auto generated photo
        profile_picture .setImageBitmap(pfpBitmap);

    }
}
